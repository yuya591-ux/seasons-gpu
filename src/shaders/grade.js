// 「記憶の風景」共通グレード＋ほんの少し水彩/絵画調（“僕の夏休み”風の温かいノスタルジー）。
// 各シェーダーの最終色に applyGrade(col, frag) を1回かける（frag=0..1の画面座標）。
// 紙の粒・周辺減光に座標が要るため第2引数 p を取る。fbm/vnoise/uResolution は
// 注入先シェーダー側で定義済みの前提（GLASS_GLSL と同じ作法。main 直前に文字列挿入）。
//
// 設計の要点（役割エージェントの合議）:
//  - 影を「藍」から「枯茶(暖色)」へ。藍の影が“不気味さ・高所の怖さ”の最大要因だった。
//  - 黒を締めず広めに持ち上げ、全体を少しリフト＝夜の重さを宵の柔らかさへ。
//  - 退色は残しつつ中間トーンの彩度を少し戻し「絵の具の発色」を保つ。
//  - ごく弱いポスタライズ＋紙の粒＋ハイライトの滲み＝写実7：水彩3で“手描き背景”の気配。

export const GRADE_GLSL = /* glsl */ `
  vec3 applyGrade(vec3 c, vec2 p) {
    float luma = dot(c, vec3(0.299, 0.587, 0.114));
    // 1) 退色（記憶のくすみ。効かせすぎない＝発色を残し霞ませない）
    c = mix(vec3(luma), c, 0.94);
    // 2) スプリットトーン: 影=枯茶(暖色)/ハイライト=飴色。藍の影をやめ寒々しさを抜く
    float sh = smoothstep(0.5, 0.0, luma);
    float hi = smoothstep(0.5, 1.0, luma);
    c += (vec3(0.156, 0.118, 0.094) - luma) * sh * 0.050;  // 枯茶 #281e18（茶のかぶりは控えめに）
    c += (vec3(1.000, 0.792, 0.627) - luma) * hi * 0.070;  // 飴色 #ffcaa0
    // 3) ゆるいSカーブ（少し強めにして抜けを良く＝モヤ感を減らす）
    c = mix(c, c * c * (3.0 - 2.0 * c), 0.20);
    // 4) 柔らかいブルーム
    c += smoothstep(0.72, 1.0, luma) * 0.035;
    // 5) 最暗部をほんの少し持ち上げる（乳白に濁らせない＝控えめ）
    c += (1.0 - smoothstep(0.0, 0.24, luma)) * 0.010;
    // 6) 全体のリフトはごく僅か（霞ませない）
    c = c * 0.992 + 0.010;

    // ===== 水彩画の質感（紙・顔料の溜まり・ウォッシュ・にじみ）=====
    float l2 = dot(c, vec3(0.299, 0.587, 0.114));
    vec2 pp = p * uResolution.xy;
    // (W1) 西日の暖色を薄く
    c += vec3(0.014, 0.004, -0.012);
    // (W2) 中間トーンの彩度を少し戻す（絵具の発色）
    float mid = smoothstep(0.15, 0.5, l2) * smoothstep(0.95, 0.55, l2);
    c = mix(vec3(l2), c, 1.0 + 0.12 * mid);
    // (W3) ウォッシュ＝平らな色面に寄せる（塗り分けの面。色相は保つ。効かせ過ぎず抜けを残す）
    float levels = 7.0;
    float ql = floor(l2 * levels + 0.5) / levels;
    vec3 wash = c * clamp(ql / max(l2, 0.04), 0.55, 1.7);
    c = mix(c, wash, 0.26);
    // (W4) tide line＝ウォッシュ境界に顔料が溜まる暗い縁（水彩の決め手。控えめに）
    float paperC = vnoise(pp * 0.011);                 // 粗い紙の目
    float band = abs(fract(l2 * levels) - 0.5) * 2.0;  // 0=境界 1=面の中央
    float tide = (1.0 - smoothstep(0.0, 0.30, band)) * (0.5 + 0.5 * paperC);
    c *= 1.0 - tide * 0.10;
    // (W5) 紙のテクスチャ（粗い目＋細かい繊維。乗算で“紙に乗った絵具”。薄く）
    float paperF = vnoise(pp * 0.052 + 11.0);
    c *= 1.0 - (mix(paperC, paperF, 0.4) - 0.5) * 0.09;
    // (W6) 顔料の粒状（granulation）。暗部ほど粒が沈む（薄く）
    float gran = fbm(pp * 0.026 + 4.0);
    c *= 1.0 - (gran - 0.5) * 0.09 * (1.0 - smoothstep(0.18, 0.62, l2));
    // (W6b) ウォッシュの濃淡ムラ（大きな筆のムラ。薄く）
    float washVar = fbm(pp * 0.006 + 20.0) - 0.5;
    c *= 1.0 + washVar * 0.06;
    // (W7) ハイライトのにじみ＋最明部に残る紙の白（白を乗せ過ぎず）
    c += smoothstep(0.72, 1.0, l2) * vec3(0.026, 0.020, 0.011) * (0.6 + 0.4 * paperC);
    c = mix(c, mix(c, vec3(1.0, 0.985, 0.95), 0.5), smoothstep(0.88, 1.0, l2) * 0.7);
    // (W8) 四隅がごく僅かに沈む（絵の縁。霞ませない程度に）
    c *= 1.0 - smoothstep(0.5, 1.05, length(p - 0.5)) * 0.08;
    return clamp(c, 0.0, 1.0);
  }
`
