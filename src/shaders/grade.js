// 「記憶の風景」共通グレード＋ほんの少し水彩/絵画調（“僕の夏休み”風の温かいノスタルジー）。
// 各シェーダーの最終色に applyGrade(col, frag) を1回かける（frag=0..1の画面座標）。
// 紙の粒・周辺減光に座標が要るため第2引数 p を取る。fbm/vnoise/uResolution は
// 注入先シェーダー側で定義済みの前提（GLASS_GLSL と同じ作法。main 直前に文字列挿入）。
//
// 設計の要点（役割エージェントの合議）:
//  - 影を「藍」から「枯茶(暖色)」へ。藍の影が“不気味さ・高所の怖さ”の最大要因だった。
//  - 黒を締めず広めに持ち上げ、全体を少しリフト＝夜の重さを宵の柔らかさへ。
//  - 退色は残しつつ中間トーンの彩度を少し戻し「絵の具の発色」を保つ。
//  - ごく弱いポスタライズ＋紙の粒＋ハイライトの滲み＝写実7：水彩3で“手描き背景”の気配。

export const GRADE_GLSL = /* glsl */ `
  vec3 applyGrade(vec3 c, vec2 p) {
    float luma = dot(c, vec3(0.299, 0.587, 0.114));
    // 1) 退色（記憶のくすみ。効かせすぎない）
    c = mix(vec3(luma), c, 0.90);
    // 2) スプリットトーン: 影=枯茶(暖色)/ハイライト=飴色。藍の影をやめ寒々しさを抜く
    float sh = smoothstep(0.5, 0.0, luma);
    float hi = smoothstep(0.5, 1.0, luma);
    c += (vec3(0.156, 0.118, 0.094) - luma) * sh * 0.055;  // 枯茶 #281e18（茶のかぶりは控えめに）
    c += (vec3(1.000, 0.792, 0.627) - luma) * hi * 0.075;  // 飴色 #ffcaa0
    // 3) ゆるいSカーブ
    c = mix(c, c * c * (3.0 - 2.0 * c), 0.14);
    // 4) 柔らかいブルーム
    c += smoothstep(0.70, 1.0, luma) * 0.04;
    // 5) 最暗部を持ち上げる（黒を締めない＝褪色フィルム。乳白に濁らせない程度に）
    c += (1.0 - smoothstep(0.0, 0.26, luma)) * 0.018;
    // 6) 全体をほんの少し明るく柔らかく（夜の重さ→宵の柔らかさ）
    c = c * 0.97 + 0.03;

    // ===== ほんの少し水彩/絵画調（僕の夏休み風）=====
    float l2 = dot(c, vec3(0.299, 0.587, 0.114));
    // (W1) 西日の暖色を薄く
    c += vec3(0.016, 0.005, -0.014);
    // (W2) 中間トーンだけ彩度を少し戻す（退色しすぎ防止＝絵の具の発色）
    float mid = smoothstep(0.15, 0.5, l2) * smoothstep(0.95, 0.55, l2);
    c = mix(vec3(l2), c, 1.0 + 0.10 * mid);
    // (W3) ごく弱いポスタライズ（絵の具の階調。段差を出さない量）
    vec3 poster = floor(c * 15.0 + 0.5) / 15.0;
    c = mix(c, poster, 0.13);
    // (W4) 紙の塗りムラ（低周波ノイズ。明部では控えめ＝白い紙の地）
    float paper = vnoise(p * uResolution.xy * 0.012) - 0.5;
    c += paper * 0.020 * (0.6 + 0.4 * (1.0 - l2));
    // (W5) ハイライトの滲み（水彩のにじみ）
    c += smoothstep(0.72, 1.0, l2) * vec3(0.03, 0.022, 0.012);
    // (W6) ごく弱い周辺減光（覗き込みの安心感・視線を中央へ）
    c *= 1.0 - smoothstep(0.45, 0.96, length(p - 0.5)) * 0.10;
    return clamp(c, 0.0, 1.0);
  }
`
