// 全情景共通の最終グレード。各シェーダーの最終色に applyGrade(col, frag) を1回かける。
// 方針（品質最優先・没入重視）: クリアでコントラストの効いた、発色の良い“抜けた”絵にする。
//  - かつての重い水彩テクスチャ（紙目・顔料粒・ウォッシュのポスタライズ）は、実解像度で
//    「ノイズ・低コントラスト・濁り」に見えていたため撤去。代わりにシネマ調のトーン＋自然な彩度。
//  - 影は寒色(藍)を避け、ごく僅かに暖色へ（高所の不気味さを出さない）。
//  - 立体感は「締まった黒・伸びたハイライト・中間の発色」で出す（霞ませない）。
// uResolution 等は注入先で定義済みの前提（main 直前に文字列挿入）。

export const GRADE_GLSL = /* glsl */ `
  vec3 applyGrade(vec3 c, vec2 p) {
    c = clamp(c, 0.0, 1.0);
    // 1) コントラストのS字（中間に締まりと抜け＝平板さ/モヤを解消し立体的に）
    c = mix(c, c * c * (3.0 - 2.0 * c), 0.32);
    // 2) 黒を締め、白を僅かに伸ばす（フィルム調の階調。眠さを抜く）
    c = (c - 0.014) * 1.055;
    // 3) 自然な彩度回復（記憶のくすみを脱し“絵具の発色”。ハイライトは飽和させない）
    float luma = dot(c, vec3(0.2126, 0.7152, 0.0722));
    float sat = 1.20 - 0.55 * smoothstep(0.68, 1.0, luma);
    c = mix(vec3(luma), c, sat);
    // 4) スプリットトーン（影=暖かな枯茶／ハイライト=飴色。ごく控えめ＝品のあるノスタルジー）
    float sh = smoothstep(0.42, 0.0, luma);
    float hi = smoothstep(0.55, 1.0, luma);
    c += vec3(0.022, 0.012, -0.004) * sh;
    c += vec3(0.024, 0.016, 0.000) * hi;
    // 5) やわらかなブルーム（最明部のにじみ＝光の存在感・空気）
    float l2 = dot(c, vec3(0.2126, 0.7152, 0.0722));
    c += smoothstep(0.80, 1.0, l2) * 0.05;
    // 6) ごく僅かな周辺減光（視線を中心へ。霞ませない程度に）
    c *= 1.0 - smoothstep(0.58, 1.18, length(p - 0.5)) * 0.16;
    return clamp(c, 0.0, 1.0);
  }
`
