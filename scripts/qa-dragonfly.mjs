import { chromium } from 'playwright'
const port = process.env.PORT || '4801'
const b = await chromium.launch()
const p = await b.newPage({ viewport: { width: 800, height: 600 }, deviceScaleFactor: 1.6 })
const errs=[]; p.on('pageerror',e=>errs.push(String(e)))
await p.goto(`http://localhost:${port}/seasons-gpu/?dev=1`, { waitUntil: 'networkidle' })
await p.locator('.gate').click().catch(()=>{})
await p.waitForTimeout(700)
await p.evaluate(()=>window.__applyScene('kitaterao-window-3d-autumn')); await p.waitForTimeout(2400)
console.log('critters:', JSON.stringify(await p.evaluate(()=>window.__town3dLoad&&window.__town3dLoad().critters)))
await p.evaluate(()=>window.__town3dFly(true)); await p.waitForTimeout(600)
await p.evaluate(()=>window.__town3dFlyPose(0,8,-20,0,-0.02)); await p.waitForTimeout(500)
await p.evaluate(()=>window.__town3dLand(true)); await p.waitForTimeout(4500)
await p.screenshot({ path:'scripts/_shots/dragonfly-autumn.png' })
console.log('state:', JSON.stringify(await p.evaluate(()=>window.__town3dJumpState())))
console.log(errs.length?'ERR '+errs.slice(0,2):'no errors')
await b.close()
