// 夜の街路灯の確認。中央の街道沿いに灯るか（夜）。昼は消灯。
import { chromium } from 'playwright'
const port = process.env.PORT || '4801'
const browser = await chromium.launch()
const page = await browser.newPage({ viewport: { width: 440, height: 900 }, deviceScaleFactor: 2 })
page.on('pageerror', (e) => console.log('ERR', e.message))
await page.goto(`http://localhost:${port}/seasons-gpu/?dev=1`, { waitUntil: 'networkidle' })
await page.locator('.gate').click().catch(() => {})
await page.waitForTimeout(600)
await page.addStyleTag({ content: '.ui{display:none !important}' })
await page.evaluate(() => window.__applyScene('kitaterao-window-3d-night'))
await page.waitForTimeout(2400)
await page.evaluate(() => window.__town3dWindow(true)); await page.waitForTimeout(900)
await page.evaluate(() => window.__town3dLean(true)); await page.waitForTimeout(1100)
await page.evaluate(() => window.__town3dFly(true)); await page.waitForTimeout(300)
await page.evaluate(() => { window.__town3dCruise(false); window.__town3dZoom(1.0); window.__town3dFlyPose(0, 9, -6, 0, -0.28) })
await page.waitForTimeout(700)
await page.screenshot({ path: 'scripts/_shots/streetlamp-0.png' })
await page.evaluate(() => { window.__town3dZoom(1.2); window.__town3dFlyPose(0, 22, -10, 0, -0.7) })
await page.waitForTimeout(700)
await page.screenshot({ path: 'scripts/_shots/streetlamp-1.png' })
await browser.close()
console.log('streetlamp done')
