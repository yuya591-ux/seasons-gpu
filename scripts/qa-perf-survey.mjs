import { chromium } from 'playwright'
const port = process.env.PORT || '4801'
const b = await chromium.launch()
const p = await b.newPage({ viewport: { width: 900, height: 560 }, deviceScaleFactor: 1.5 })
const errs=[]; p.on('pageerror',e=>errs.push(String(e)))
await p.goto(`http://localhost:${port}/seasons/?dev=1`, { waitUntil: 'networkidle' })
await p.locator('.gate').click().catch(()=>{})
await p.waitForTimeout(800)
await p.evaluate(()=>window.__applyScene('kitaterao-window-3d-sunset'))
await p.waitForTimeout(2800)
const stat = async (label) => { await p.waitForTimeout(600); const d = await p.evaluate(()=>window.__town3dDraw&&window.__town3dDraw()); console.log(label.padEnd(16), 'calls='+String(d?.calls).padStart(4), 'tris='+String(d?.tris).padStart(7), 'progs='+d?.progs, 'tex='+d?.texMem, 'geo='+d?.geoMem) }
await stat('indoor/window')
await p.evaluate(()=>window.__town3dFly(true)); await p.waitForTimeout(800)
await p.evaluate(()=>window.__town3dCruise(false))
await p.evaluate(()=>window.__town3dFlyPose(0,42,30,0,-0.28)); await stat('fly home low')
await p.evaluate(()=>window.__town3dFlyPose(0,120,30,0,-0.5)); await stat('fly home high')
await p.evaluate(()=>window.__town3dFlyPose(0,170,30,0,-0.7)); await stat('cloud sea')
await p.evaluate(()=>window.__town3dFlyPose(640,30,-46,1.3,0.0)); await stat('fly Edo')
await p.evaluate(()=>window.__town3dFlyPose(140,30,-640,0,0.0)); await stat('fly Sengoku')
await p.evaluate(()=>window.__town3dFlyPose(-640,30,-30,0,0.0)); await stat('fly Taisho')
console.log('load(per-frame counts):', JSON.stringify(await p.evaluate(()=>window.__town3dLoad&&window.__town3dLoad())))
console.log('eraCull:', JSON.stringify(await p.evaluate(()=>window.__town3dEraCull&&window.__town3dEraCull())))
console.log(errs.length?'ERR '+errs.slice(0,2):'no errors')
await b.close()
